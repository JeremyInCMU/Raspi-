#include "math.h"

/*
 * Add
 */
int add (int factor_1, int factor_2) {
   
    return (factor_1 + factor_2);
}

/*
 * Sub
 */
int sub (int factor_1, int factor_2) {
    return (factor_1 - factor_2);
}

/*
 * Mul
 */
int mul (int factor_1, int factor_2) {
    return (factor_1 * factor_2);
}

/*
 * Div
 */
int div (int factor_1, int factor_2) {
    return (factor_1 / factor_2);
}

/*
 * Mod
 */
int mod (int factor_1, int factor_2) {
    return (factor_1 % factor_2);
}

